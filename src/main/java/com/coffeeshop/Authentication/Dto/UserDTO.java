package com.coffeeshop.Authentication.Dto;

public class UserDTO {
    private Long id;
    private String username;
    private String password;
    private String role;
    private UserDTO(Long id, String username, String password, String role) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.role = role;
    }
    private UserDTO() {
    }
    public static UserDTO createParameterDTO(Long id, String username, String password, String role){
        return new UserDTO(id, username, password, role);
    }
    public static UserDTO createEmptyDTO(){
        return new UserDTO();
    }
    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
    public String getUsername() {
        return username;
    }
    public void setUsername(String username) {
        this.username = username;
    }
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }
    public String getRole() {
        return role;
    }
    public void setRole(String role) {
        this.role = role;
    }
    @Override
    public String toString() {
        return "UserDTO{" +
                "id=" + id +
                ", username='" + username + '\'' +
                ", password='" + password + '\'' +
                ", role='" + role + '\'' +
                '}';
    }
}
